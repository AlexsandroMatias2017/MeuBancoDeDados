package qacademico.entidades;

public class Aluno
{
	int idAluno;
	private String nome;
	private String sobrenome;
	private String telefone;

	// Getters and Setters
	public int getIdAluno()
	{
		return idAluno;
	}

	public String getNome()
	{
		return nome;
	}

	public String getSobrenome()
	{
		return sobrenome;
	}

	public String getTelefone()
	{
		return telefone;
	}
}
